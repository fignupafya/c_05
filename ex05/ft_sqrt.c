int	ft_sqrt(int nb)
{
	int	a;

	if (nb < 0)
		return (0);
	a = 0;
	while (a != 46341 && a * a <= nb)
	{
		if (a * a == nb)
			return (a);
		a++;
	}
	return (0);
}
